     System.out.println("Hello Java");  <br>
    }  <br>
}  <br>

<h3>Application</h3><br>
According to Sun, 3 billion devices run Java. There are many devices where Java is currently used.<br> Some of them are as follows:<br>

1.Desktop Applications such as acrobat reader, media player, antivirus, etc.<br>
2.Web Applications such as irctc.co.in, javatpoint.com, etc.<br>
3.Enterprise Applications such as banking applications.<br>
4.Mobile<br>
5.Embedded System<br>
6.Smart Card<br>
7.Robotics<br>
8,Games, etc.<br>
Types of Java Applications<br>
There are mainly 4 types of applications that can be created using Java programming:<br>

1) Standalone Application<br>
Standalone applications are also known as desktop applications or window-based applications. <br>
These are traditional software that we need to install on every machine. <br>
Examples of standalone application are Media player, antivirus, etc. AWT and<br>
 Swing are used in Java for creating standalone applications.<br>

2) Web Application<br>
An application that runs on the server side and creates a dynamic page is called a web application. Currently, <br>
Servlet, JSP, Struts, Spring, Hibernate, JSF, etc. technologies are used for creating web applications in Java.<br>

3) Enterprise Application<br>
An application that is distributed in nature, such as banking applications, etc. is called enterprise application.<br>
 It has advantages of the high-level security, load balancing, and clustering. In Java, EJB is used for
 creating enterprise applications.<br>

4) Mobile Application<br>
An application which is created for mobile devices is called a mobile application. Currently,
 Android and Java ME are used for creating mobile applications.<br>

Java Platforms / Editions<br>
There are 4 platforms or editions of Java:<br>

1) Java SE (Java Standard Edition)<br>
It is a Java programming platform. It includes Java programming APIs such as java.lang, java.io,<br> 
java.net, java.util, java.sql, java.math etc. It includes core topics like OOPs, String, Regex,<br> 
Exception, Inner classes, Multithreading, I/O Stream, Networking, AWT, Swing, Reflection, Collection, etc.<br>

2) Java EE (Java Enterprise Edition)<br>
It is an enterprise platform which is mainly used to develop web and enterprise applications.<br>
 It is built on the top of the Java SE platform. It includes topics like Servlet, JSP, Web Services, EJB, JPA, etc.<br>

3) Java ME (Java Micro Edition)<br>
It is a micro platform which is mainly used to develop mobile applications.<br>

4) JavaFX<br>
It is used to develop rich internet applications. It uses a light-weight user interface API.

Prerequisite<br>
To learn Java, you must have the basic knowledge of C/C++ programming language.

Audience<br>

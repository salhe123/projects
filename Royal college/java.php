<b><i>What is Java</i></b>
Java is a programming language and a platform.<br>

Java is a high level, robust, object-oriented and secure programming language.<br>

Platform: Any hardware or software environment in which a program runs, is known as a platform.<br>
 Since Java has a runtime environment (JRE) and API, it is called a platform.<br>
 <h3>Example</h3><br>
 class Simple{  <br>

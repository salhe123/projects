<b><i>Computing</i></b> is any goal-oriented activity requiring, benefiting from, or
 creating computing machinery.<br>
 It includes the study and experimentation of algorithmic processes and development of <br>
 both hardware and software.<br>
  It has scientific, engineering, mathematical, technological and social aspects. <br>
  Major computing disciplines include computer engineering, computer science, cybersecurity, <br>
  data science, information systems, information technology and software engineering.<br>
  The history of computing is longer than the history of computing hardware and modern <br>
  computing technology and includes the history of methods intended<br>
   for pen and paper or for chalk and slate, with or without the aid of tables.<br>

Computing is intimately tied to the representation of numbers. But long before abstractions <br>
like the number arose, there were mathematical concepts to serve the purposes of civilization.<br>
These concepts include one-to-one correspondence (the basis of counting), comparison to a standard <br>
(used for measurement), and the 3-4-5 right triangle (a device for assuring a right angle).<br>

The earliest known tool for use in computation was the abacus, and it was thought to have<br>
 been invented in Babylon circa 2400 BC. Its original style of usage was by lines drawn in sand with pebbles. <br> more modern design, are still used as calculation tools today. This was the first known calculation aid –<br> preceding Greek methods by 2,000 years{{ computer|reason=What Greek methods?|date=March 2018}}.<br>

The first recorded idea of using digital electronics for computing was the 1931 paper<br>  
"The Use of Thyratrons for High Speed Automatic Counting of Physical Phenomena" by C. E. Wynn-Williams.<br>
[4] Claude Shannon's 1938 paper "A Symbolic Analysis of Relay and Switching Circuits" then introduced <br>
the idea ofusing electronics for Boolean algebraic operations.<br>

The concept of a field-effect transistor was proposed by Julius Edgar Lilienfeld in 1925.<br>
 John Bardeen and Walter Brattain, while working under William Shockley at Bell Labs, built  <br>
 the first working transistor,the point-contact transistor, in 1947.[5][6] In 1953, the University of<br> 
 Manchester built the first transistorized computer, called the Transistor Computer.[7] However, <br>
 early junction transistors were relatively bulky devices that were difficult to manufacture on<br>
  a mass-production basis, which limited them to a number of specialised <br>applications.
 The metal–oxide–silicon field-effect transistor (MOSFET, or MOS transistor) was invented by<br>
  Mohamed Atalla and Dawon Kahng at Bell Labs in 1959.[9][10] compact transistor that could be <br>
  miniaturised and mass-produced for a wide range of uses.[8] The MOSFET made it<br>
   possible to build high-density integrated circuit chips,[11][12] leading to what is<br> 
   known as the computer revolution[13] or microcomputer revolution.<br>

Computer Main articles: Computer, Outline of computers, and Glossary of computer terms<br>
A computer is a machine that manipulates data according to a set of instructions<br>
 called a computer program. The program has an executable form that the computer can<br>
  use directly to execute the instructions. The same program in its human-readable source<br>
   code form, enables a programmer to study and develop a sequence of steps known as an algorithm.<br>
    Because the instructions can be carried out in different types of computers,<br> 
    a single set of source instructions converts to machine instructions according to the CPU type.<br>

The execution process carries out the instructions in a computer program.Instructions express<br>
 the computations performed by the computer. They trigger sequences of simple actions on the<br>
 o executing machine. Those actions produce effects according to the semantics of the instructions.<br>

